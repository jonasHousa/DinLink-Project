///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Prevent the section name from showing in the toolbar -------------------------------------------------------------------------------
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
document.addEventListener("DOMContentLoaded", function () {
    // Get all menu items in the navigation bar
    var menuItems = document.querySelectorAll("nav a");

    // Add click event listener to each menu item
    menuItems.forEach(function (item) {
        item.addEventListener("click", function () {
            // Update document title to an empty string
            document.title = "";
        });
    });
});





////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Single Page Scrolling Script ----------------------------------------------------------------------------------------------------------------
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
var accordions = document.getElementsByClassName("accordion");

for (var i = 0; i < accordions.length; i++) {
    accordions[i].addEventListener("click", function () {
        this.classList.toggle("active"); var panel = this.nextElementSibling; if (panel.style.maxHeight) {
            panel.style.maxHeight = null;
        } else { panel.style.maxHeight = panel.scrollHeight + "px"; }
    });
}



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Secondary Navigation (ropdown menu navigate)----------------------------------------------------------------------------------------------------------------
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function navigateSidebar(selectElement) {
    var selectedUrl = selectElement.value;
    window.location.href = selectedUrl;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Side Bar Menu----------------------------------------------------------------------------------------------------------------
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// === ( Icon )
function showSidebar() {
    const sidebar = document.querySelector(".sidebarNavigation")
    sidebar.style.display = 'flex'
}

// X ( Icon )
function hideSidebar() {
    const sidebar = document.querySelector(".sidebarNavigation")
    sidebar.style.display = 'none'
}



